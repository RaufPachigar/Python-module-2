#What are oops concepts? Is multiple inheritance supported in java


# OOPs (Object-Oriented Programming) Concepts:

# Class: Blueprint for creating objects (contains attributes and methods).
# Object: Instance of a class.
# Encapsulation: Restricting access to certain details and protecting object integrity by bundling data with methods.
# Inheritance: A class can inherit methods and properties from another class.
# Polymorphism: Ability to take many forms (e.g., method overriding).
# Abstraction: Hiding implementation details and showing only the essential features.
# Multiple Inheritance in Java:

# Java does not support multiple inheritance directly for classes (i.e., a class cannot inherit from more than one class). 
# This restriction avoids ambiguity (the diamond problem).
# However, multiple inheritance is allowed with interfaces in Java.
#Explain Exception handling? What is an Error in Python?

# Exception handling is the process of responding to exceptions (runtime errors) 
# by using the try-except block. This allows the program to handle unexpected conditions
# and errors gracefully without crashing.

# Example:


# try:
#     x = 10 / 0
# except ZeroDivisionError:
#     print("You can't divide by zero!")


# Errors in Python are issues in the code that can prevent execution.
# Common types include syntax errors (e.g., incorrect code structure)
# and exceptions (e.g., ZeroDivisionError, TypeError).
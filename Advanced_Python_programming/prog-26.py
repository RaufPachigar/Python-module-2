# What is Instantiation in terms of OOP terminology


# Instantiation is the process of creating an object (instance) of a class.
# When you call a class, Python calls its __init__ method to initialize the object,
# and this is referred to as instantiating the class.

# Example:

# Instantiate the class 'Rectangle'

# rect = Rectangle(10, 5)
# Here, rect is an instance of the Rectangle class.
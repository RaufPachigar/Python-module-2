# File function in Python
# In Python, file handling is done using the built-in open() function, which allows you to create, read, write, and append to files. The syntax is:


# file = open("filename", mode)


# mode:
# 'r': Read (default)
# 'w': Write (create or overwrite)
# 'a': Append
# 'x': Create a new file
# 't': Text mode (default)
# 'b': Binary mode